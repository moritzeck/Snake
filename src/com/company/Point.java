package com.company;

public class Point {
    // Fields
    private int x = 0;
    private int y = 0;

    // Constructors
    public Point() {

    }

    public Point(int x, int y) {
        this.x = x;
        this.y = y;
    }

    // Getter
    public int getX() {
        return x;
    }

    public int getY() {
        return y;
    }
}
